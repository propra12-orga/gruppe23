public class Hulk {
	// Deklaration & Initialisierung:
	public int x = 1; // x-Koordinate (horizontale Position)
	public int y = 1; // y-Koordinate (vertikale Position)
	
	// setter & getter:
	public int get_x() {
		return x;
	}
	
	public void set_x(int x) {
		this.x = x;
	}
	
	public int get_y() {
		return y;
	}
	
	public void set_y(int y) {
		this.y = y;
	}
	
}
